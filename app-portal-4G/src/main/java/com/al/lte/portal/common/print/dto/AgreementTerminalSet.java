package com.al.lte.portal.common.print.dto;

public class AgreementTerminalSet {
	private String custName;
	private String accessNumber;
	private String offerSpecName;
	private String offerSpecDesc;
	public String getCustName() {
		return custName;
	}
	public void setCustName(String custName) {
		this.custName = custName;
	}
	public String getAccessNumber() {
		return accessNumber;
	}
	public void setAccessNumber(String accessNumber) {
		this.accessNumber = accessNumber;
	}
	public String getOfferSpecName() {
		return offerSpecName;
	}
	public void setOfferSpecName(String offerSpecName) {
		this.offerSpecName = offerSpecName;
	}
	public String getOfferSpecDesc() {
		return offerSpecDesc;
	}
	public void setOfferSpecDesc(String offerSpecDesc) {
		this.offerSpecDesc = offerSpecDesc;
	}
}
